public class Player {
    public String position; 
    public String name;
    public String age; 
    public String jersey; 
    public String ab;
    public String avg; 
    public String rbi;
    public String hr;
    public Player(String position, String name, String age, String jersey, String ab, String avg, String rbi, String hr){
        this.position = position;
        this.name = name;
        this.age = age;
        this.jersey = jersey; 
        this.ab = ab;
        this.avg = avg;
        this.rbi = rbi; 
        this.hr = hr; 
    }
}
